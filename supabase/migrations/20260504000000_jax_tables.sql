-- ── Row Level Security ─────────────────────────────────────────────────────────
-- Enable RLS on all new tables. Service role bypasses all policies.
-- Application-level access is controlled by Netlify function authentication.

ALTER TABLE jax_documents        ENABLE ROW LEVEL SECURITY;
ALTER TABLE agent_conversations  ENABLE ROW LEVEL SECURITY;
ALTER TABLE jax_form_registry    ENABLE ROW LEVEL SECURITY;
ALTER TABLE jax_notifications    ENABLE ROW LEVEL SECURITY;

-- jax_documents: authenticated users can read their own documents; service role can do all
CREATE POLICY IF NOT EXISTS "jax_documents_select_own"
  ON jax_documents FOR SELECT
  USING (auth.uid() = created_by);

CREATE POLICY IF NOT EXISTS "jax_documents_insert_own"
  ON jax_documents FOR INSERT
  WITH CHECK (auth.uid() = created_by);

CREATE POLICY IF NOT EXISTS "jax_documents_update_own"
  ON jax_documents FOR UPDATE
  USING (auth.uid() = created_by);

-- agent_conversations: users can only access their own conversations
CREATE POLICY IF NOT EXISTS "agent_conversations_select_own"
  ON agent_conversations FOR SELECT
  USING (auth.uid() = user_id);

CREATE POLICY IF NOT EXISTS "agent_conversations_insert_own"
  ON agent_conversations FOR INSERT
  WITH CHECK (auth.uid() = user_id);

CREATE POLICY IF NOT EXISTS "agent_conversations_update_own"
  ON agent_conversations FOR UPDATE
  USING (auth.uid() = user_id);

-- jax_form_registry: readable only by admin+ roles (contains internal table/field schema)
-- Staff and field_agents access forms via the submit_form tool (server-side, service role)
CREATE POLICY IF NOT EXISTS "jax_form_registry_select_admin"
  ON jax_form_registry FOR SELECT
  USING (
    EXISTS (
      SELECT 1 FROM admin_users_1777025000000
      WHERE user_id = auth.uid()
        AND role IN ('sysadmin', 'admin')
    )
  );

-- jax_notifications: users can read their own notifications
CREATE POLICY IF NOT EXISTS "jax_notifications_select_own"
  ON jax_notifications FOR SELECT
  USING (auth.uid() = recipient_id);

CREATE POLICY IF NOT EXISTS "jax_notifications_update_own"
  ON jax_notifications FOR UPDATE
  USING (auth.uid() = recipient_id);
