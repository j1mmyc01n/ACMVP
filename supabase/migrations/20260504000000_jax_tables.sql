-- Creates:
--   jax_documents       — AI-generated clinical and administrative documents
--   agent_conversations — Conversation history for the Jax AI assistant
--   jax_form_registry   — Registry of platform forms the AI can fill/submit
--   jax_notifications   — In-platform notifications created by the monitor handler
--
-- Alters:
--   clients_1777020684735 — Adds clinical_notes jsonb column for AI-updated notes
--
-- All changes are idempotent (IF NOT EXISTS / ADD COLUMN IF NOT EXISTS).
